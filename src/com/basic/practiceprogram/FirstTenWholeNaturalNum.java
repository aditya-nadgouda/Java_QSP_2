package com.basic.practiceprogram;

public class FirstTenWholeNaturalNum {

    public static void main(String[] args) {

        System.out.println("Whole Numbs : ");

        int i = 0;
        while (i<10){
            System.out.println(i++);
        }

        System.out.println("Natural Numbs : ");
        int j = 0;
        while (j<=10){
            System.out.println(j++);
        }

    }
}
